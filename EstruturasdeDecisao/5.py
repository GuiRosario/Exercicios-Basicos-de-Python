numero = int(input('Digite um número:'))

if(numero == 1):
    print('Segunda')
elif(numero == 2):
    print('Terça')
elif(numero == 3):
    print('Quarta')
elif(numero == 4):
    print('Quinta')
elif(numero == 5):
    print('Sexta')
elif(numero == 6):
    print('Sabado')
elif(numero == 7):
    print('Domingo')
else:
    print('Número inválido!')